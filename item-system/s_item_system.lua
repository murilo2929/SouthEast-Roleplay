function isWearingBadge(element, item)
	if item then
		if isBadge(item) then
			if(getElementData(source, badges[item][1])) then
				return true
			else
				for key, badge in pairs ( badges ) do
					if key ~= item then
						if ( getElementData ( source, badge[1] ) ) then
							return true
						end
					end
				end
			end
		end
	else
		for key, badge in pairs ( badges ) do
			if ( getElementData ( source, badge[1] ) ) then
				return true
			end
		end
	end
	return false
end

